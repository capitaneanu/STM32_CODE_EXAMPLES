  /* USER CODE BEGIN 2 */
  printMsg("\r\nTest mixed normal and DMA SPI writes to/read from  Winbond W25Q16 serial flash\r\n\r\n");

  CHIP_DESELECT();

  // init code reads flash id and hence determines the page/sector/block sizes
  if (false == W25qxx_Init()) {
	  Error_Handler();
  	  }

  // Winbond W25Q16 serial flash : number pages = 8192, page size = 256 bytes

  // write  source buffer DataSPI with normal SPI transaction
  // write address 250 translates to page 0, offset 250
  // Note : we  have to write to both page 0 and page 1 as the
  // number of bytes in DataSPI will cross the page boundary
  uint32_t writeAddressSPI = 250;

  // write source buffer WriteDataDMA  with DMA SPI transfer
  // write address 895 translates to page 3, offset 127
  // Note : write stays within the page boundary
  uint32_t writeAddressDMA = 895;


#ifdef TEST_WRITE_FLASH
  // Erase sector 0 (W25Q16 sector size = 4096 bytes)
  W25qxx_EraseSector(0);

  printMsg("Use normal SPI to write DataSPI : %s", DataSPI);
  W25qxx_WriteBytes(DataSPI, writeAddressSPI, sizeof(DataSPI));

  printMsg("Use DMA to write DataSPIDMA : %s", DataSPIDMA);
  uint32_t pageAddressDMA = writeAddressDMA/256;
  uint32_t pageOffsetDMA = writeAddressDMA%256;
  W25qxx_WritePageDMA(DataSPIDMA, pageAddressDMA, pageOffsetDMA, sizeof(DataSPIDMA));
#endif


  // for reads, can read unlimited number of bytes, no issue with crossing
  // page boundaries

  printMsg("\r\nUse normal SPI  to read back the normal SPI write\r\n");
  W25qxx_ReadBytes(ReadData, writeAddressSPI, sizeof(DataSPI));
  printMsg((char*)ReadData);

  printMsg("\r\nUse DMA to read back the normal SPI write\r\n");
  W25qxx_ReadBytesDMA(ReadData, writeAddressSPI, sizeof(DataSPI));
  //wait for DMA to complete
  while (w25qxx.Lock == 0) {}
  printMsg((char*)ReadData);


  printMsg("\r\nUse normal SPI  to read back the DMA write\r\n");
  W25qxx_ReadBytes(ReadData, writeAddressDMA, sizeof(DataSPIDMA));
  printMsg((char*)ReadData);

  printMsg("\r\nUse DMA to read back the DMA write\r\n");
  W25qxx_ReadBytesDMA(ReadData, writeAddressDMA, sizeof(DataSPIDMA));
  //wait for DMA to complete
  while (w25qxx.Lock == 0) {}
  printMsg((char*)ReadData);

  /* USER CODE END 2 */
 
 

