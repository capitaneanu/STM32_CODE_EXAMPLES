char msg[50];

// this callback is common to both cpu isr and dma transfer
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc){
   uint32_t accum = 0;
   for (int inx = 0; inx < 16; inx++) {
	   accum += (uint32_t)destinationBuffer[inx];
   	   }
   accum /= 16;
   // from hal code, when int temperature sensor is used, vref = vrefint
   // value = 1.21V from datasheet
   float vref = 1.21f;
   float vsense = ((float)accum * vref)/4095.0f;
   // from datasheet
   float v25 = 0.76f;
   float avgSlope = 0.0025f;
   // from ref manual
   float temperatureC = 25.0f + (vsense - v25)/avgSlope;
   // add _u_printf_float to linker flags to print floats
   sprintf(msg, "Internal temperature : %3.2f C\r\n", temperatureC);
   HAL_UART_Transmit(&huart1, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
}

